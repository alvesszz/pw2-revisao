
public class ExercicioSete {

	public static void main(String[] args) {
		int triangulo = 24;
		if ((triangulo * 3) / 3 == 10)
		{
			System.out.println(" Equilatero");
			
		}
		if ((triangulo * 2) / 2 == 10)
		{
			System.out.println(" Isosceles");
		}
		else 
			System.out.println(" Escaleno");
		
	}
}
